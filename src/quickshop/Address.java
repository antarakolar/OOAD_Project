package Quickshop;

public class Address {
	private String streetNumber;
	private String streetName;
	private String aptNumber;
	private String city;
	private String state;
	private String zip;
	protected String getStreetNumber() {
		return streetNumber;
	}
	protected void setStreetNumber(String streetNumber) {
		this.streetNumber = streetNumber;
	}
	protected String getStreetName() {
		return streetName;
	}
	protected void setStreetName(String streetName) {
		this.streetName = streetName;
	}
	protected String getAptNumber() {
		return aptNumber;
	}
	protected void setAptNumber(String aptNumber) {
		this.aptNumber = aptNumber;
	}
	protected String getCity() {
		return city;
	}
	protected void setCity(String city) {
		this.city = city;
	}
	protected String getState() {
		return state;
	}
	protected void setState(String state) {
		this.state = state;
	}
	protected String getZip() {
		return zip;
	}
	protected void setZip(String zip) {
		this.zip = zip;
	}
	
	

}
